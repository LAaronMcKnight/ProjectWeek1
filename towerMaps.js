const towers = []


class Tower {
    constructor({ position = { x: 0, y: 0 } }){
        this.position = position
        this.width = 12 * 2
        this.height = 12 * 2
        this.center = {
            x: this.position.x + this.width / 2,
            y: this.position.y + this.height / 2
        }
        this.projectiles = [
            new Projectile({
                position: {
                    x: this.center.x,
                    y: this.center.y
                },
                enemy: enemies[0]
            })
        ]
    }
    
    draw() {
        cvs.fillStyle = 'red'
        cvs.fillRect(this.position.x, this.position.y, this.width, this.height)
        
    }
}


class Projectile {
    constructor({position = {x: 0, y: 0}, enemy}) {
        this.position = position
        this.velocity = { x: 0, y: 0 }
        this.enemy = enemy
        this.radius = 6
    }

    draw() {
        cvs.beginPath()
        cvs.arc(this.position.x, this.position.y, this.radius, 0, Math.PI * 2)
        cvs.fillStyle = "blue"
        cvs.fill()
    }

    update() {
        this.draw()

        const angle = Math.atan2(enemies[0].center.y - this.position.y, enemies[0].center.x - this.position.x)

        this.velocity.x = Math.cos(angle)
        this.velocity.y = Math.sin(angle)

        this.position.x += this.velocity.x
        this.position.y += this.velocity.y
    }
}

